//User function template for C++
class Solution{
public:	
	int matSearch (vector <vector <int>> &arr, int n, int m, int target)
	{
	    for(int i=0;i<arr.size();i++){
            for(int j=0;j<arr[0].size();j++){
                if(arr[i][j] == target) return true;
            }
        }
        return false;
	}
};

